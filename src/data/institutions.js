// Returns top matches for a query string (min 3 chars)
export function searchInstitutions(query, maxResults = 10) {
    if (!query || query.trim().length < 3) return [];
    const q = query.toLowerCase().trim();
    const exact = [];
    const starts = [];
    const contains = [];
    for (const name of INSTITUTIONS) {
        const lc = name.toLowerCase();
        if (lc === q) exact.push(name);
        else if (lc.startsWith(q)) starts.push(name);
        else if (lc.includes(q)) contains.push(name);
        if (exact.length + starts.length + contains.length >= maxResults * 3) break;
    }
    return [...exact, ...starts, ...contains].slice(0, maxResults);
}
